package exam.questions1;

public class EmployeeInfo {
    String empID;
    String empName;
    String empAddress;
    long empMobileNo;
    String empQualification;
    String empStatus;
    String username;
    String password;


    public String getEmpID() {
        return empID;
    }
    public void setEmpID(String empID) {
        this.empID = empID;
    }

    public String getEmpName() {
        return empName;
    }

    public void setEmpName(String empName) {
        this.empName = empName;
    }

    public String getEmpAddress() {
        return empAddress;
    }

    public void setEmpAddress(String empAddress) {
        this.empAddress = empAddress;
    }

    public double getEmpMobileNo() {
        return empMobileNo;
    }

    public void setEmpMobileNo(long empMobileNo) {
        this.empMobileNo = empMobileNo;
    }

    public String getEmpQualification() {
        return empQualification;
    }

    public void setEmpQualification(String empQualification) {
        this.empQualification = empQualification;
    }

    public String getEmpStatus() {
        return empStatus;
    }

    public void setEmpStatus(String empStatus) {
        this.empStatus = empStatus;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
}
