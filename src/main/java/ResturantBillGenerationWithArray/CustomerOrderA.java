package ResturantBillGenerationWithArray;

public class CustomerOrderA {
    String items;
    int noOfEachItems;

    public CustomerOrderA(String items, int noOfEachItems) {
        this.items = items;
        this.noOfEachItems = noOfEachItems;
    }

    public String getItems() {
        return items;
    }

    public int getNoOfEachItems() {
        return noOfEachItems;
    }

}
